    private void jbtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCrearActionPerformed
        // TODO add your handling code here:
        if(jpgsProgreso.getValue()<100){
            javax.swing.JOptionPane.showMessageDialog(this, jpgsProgreso.getToolTipText(), "Formulario incompleto", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            String nombre = jftmNombre.getText();
            String historia = jtxtaHistoria.getText();
            String contraseña = String.valueOf(jpswContraseña.getPassword());
            int experiencia = jsldExperiencia.getValue();
            Object nivelObj = jspnNivel.getValue();
            int nivel = (nivelObj instanceof Integer) ? (Integer)nivelObj : Integer.parseInt(nivelObj.toString());
            javax.swing.JPanel panel = new javax.swing.JPanel();
            panel.setLayout(new java.awt.GridLayout(0,1));
            panel.add(new javax.swing.JLabel("<html><b>Nombre:</b> "+nombre+"</html>"));
            panel.add(new javax.swing.JLabel("<html><b>Nivel:</b> "+nivel+"</html>"));
            panel.add(new javax.swing.JLabel("<html><b>Experiencia:</b> "+experiencia+"</html>"));
            panel.add(new javax.swing.JLabel("<html><b>Contraseña:</b> "+contraseña+"</html>"));
            panel.add(new javax.swing.JLabel("<html><b>Historia:</b> "+historia+"</html>"));
            javax.swing.JOptionPane.showMessageDialog(this, panel, "✅ Personaje Creado", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            // Limpiar campos solo si el registro fue exitoso
            jftmNombre.setText("");
            jtxtaHistoria.setText("");
            jpswContraseña.setText("");
            jspnNivel.setValue(0);
            jsldExperiencia.setValue(0);
        } catch(NumberFormatException ex){
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error en el formato de número: " + ex.getMessage(),
                "Error de datos",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch(IllegalArgumentException ex){
            javax.swing.JOptionPane.showMessageDialog(this,
                "Datos inválidos: " + ex.getMessage(),
                "Error de validación",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch(Exception ex){
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error inesperado: " + ex.getMessage(),
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbtnCrearActionPerformed

