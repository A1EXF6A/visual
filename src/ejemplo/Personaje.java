    public void progreso(){
        int progreso = 0;
        try {
            String nombre = jftmNombre.getText().trim();
            String historia = jtxtaHistoria.getText().trim();
            int fuerza = calcularFuerzaContraseña(String.valueOf(jpswContraseña.getPassword()));
            Object nivelObj = jspnNivel.getValue();
            int nivel = (nivelObj instanceof Integer) ? (Integer)nivelObj : Integer.parseInt(nivelObj.toString());
            int experiencia = jsldExperiencia.getValue();
            if(nombre.length()>=3) progreso+=20;
            if(historia.length()>=10) progreso+=20;
            if(fuerza>=2) progreso+=20;
            if(nivel>0) progreso+=20;
            if(experiencia>1) progreso+=20;
            jpgsProgreso.setValue(progreso);
            mostrarValidaciones(nombre, historia, fuerza, nivel, experiencia);
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Error al calcular el progreso: " + ex.getMessage(),
                "Error de datos",
                JOptionPane.ERROR_MESSAGE);
        }
    }

    private int calcularFuerzaContraseña(String pass) {
    if (pass == null || pass.isEmpty()) {
        return 0; // Contraseña vacía o nula
    }

    int fuerza = 0;

    if (pass.length() >= 6) fuerza++;
    if (pass.matches(".*[A-Z].*")) fuerza++; // Contiene mayúsculas
    if (pass.matches(".*[0-9].*")) fuerza++; // Contiene números
    if (pass.matches(".*[!@#$%^&*()_+\\-=].*")) fuerza++; // Contiene caracteres especiales

    return fuerza;
}


    private void mostrarValidaciones(String nombre, String historia, int fuerza, int nivel, int experiencia) {
        StringBuilder msg = new StringBuilder();
        if(nombre.length()<3) msg.append("\n- El nombre debe tener al menos 3 caracteres.");
        if(historia.length()<10) msg.append("\n- La historia debe tener al menos 10 caracteres.");
        if(fuerza<2) msg.append("\n- La contraseña debe tener al menos 6 caracteres y una mayúscula o número.");
        if(nivel<1) msg.append("\n- El nivel debe ser mayor a 0.");
        if(experiencia<2) msg.append("\n- La experiencia debe ser mayor a 1.");
        jpgsProgreso.setToolTipText(msg.length()>0 ? "Faltan requisitos:"+msg.toString() : "¡Formulario completo!");
    }
