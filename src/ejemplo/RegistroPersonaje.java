    private void actualizarProgreso() {
        int progreso = 0;
        try {
            // Nombre
            if (!ftmNombre.getText().trim().isEmpty()) {
                progreso += 20;
            }
            // Clave
            if (pswClave.getPassword().length > 0) {
                progreso += 20;
            }
            // Historia
            if (!txaHistoria.getText().trim().isEmpty()) {
                progreso += 20;
            }
            // Nivel (Slider)
            if (jsldNivel.getValue() > 1) {
                progreso += 20;
            }
            // Vidas (Spinner)
            Object vidasObj = spnVidas.getValue();
            int vidas = 0;
            if (vidasObj instanceof Integer) {
                vidas = (Integer) vidasObj;
            } else {
                vidas = Integer.parseInt(vidasObj.toString());
            }
            if (vidas > 0) {
                progreso += 20;
            }
            prfProgreso.setValue(progreso);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Error al actualizar el progreso: " + ex.getMessage(),
                "Error de datos",
                JOptionPane.ERROR_MESSAGE);
        }
    }

