    private void jbtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCrearActionPerformed
        // TODO add your handling code here:
        if (prfProgreso.getValue() < 100) {
            JOptionPane.showMessageDialog(this,
                    "Faltan datos para completar el registro del personaje.",
                    "Formulario incompleto",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            // Si está completo, obtener datos
            String nombre = ftmNombre.getText();
            String clave = new String(pswClave.getPassword());
            String historia = txaHistoria.getText();
            int nivel = jsldNivel.getValue();
            Object vidasObj = spnVidas.getValue();
            int vidas = 0;
            if (vidasObj instanceof Integer) {
                vidas = (Integer) vidasObj;
            } else {
                vidas = Integer.parseInt(vidasObj.toString());
            }
            // Validaciones adicionales
            if (nombre.trim().isEmpty() || clave.trim().isEmpty() || historia.trim().isEmpty()) {
                throw new IllegalArgumentException("Algún campo está vacío.");
            }
            if (nivel < 1 || vidas < 1) {
                throw new IllegalArgumentException("Nivel o vidas no válidos.");
            }
            // Mostrar datos
            JOptionPane.showMessageDialog(this,
                    "✅ Personaje creado con éxito:\n\n" +
                    "Nombre: " + nombre + "\n" +
                    "Clave: " + clave + "\n" +
                    "Historia: " + historia + "\n" +
                    "Nivel: " + nivel + "\n" +
                    "Vidas: " + vidas,
                    "Personaje Creado",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error en el formato de número: " + ex.getMessage(),
                    "Error de datos",
                    JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this,
                    "Datos inválidos: " + ex.getMessage(),
                    "Error de validación",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Error inesperado: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        // Limpiar formulario
ftmNombre.setText("");
pswClave.setText("");
txaHistoria.setText("");
jsldNivel.setValue(0);
spnVidas.setValue(0);
prfProgreso.setValue(0);

    }//GEN-LAST:event_jbtnCrearActionPerformed

